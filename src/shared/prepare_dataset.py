"""
prepare_dataset.py
==================
Shared dataset preparation for HammerTime binary and multi-class pipelines.

Reads from data/classes/<class>/ subdirectories and data/labels/.
Source label files are never modified. All remapping happens in memory
when writing to dataset/.

Modes
-----
Multi-class (default):
    All class labels preserved as-is in dataset/labels/.
    python src/shared/prepare_dataset.py

Binary (--target_class):
    Target class labels remapped to class 0 in dataset/labels/.
    All other labeled images written with empty label files (background).
    python src/shared/prepare_dataset.py --target_class hammer

Data layout
-----------
data/
    classes/
        hammer/
        axe/
        wrench/
        pickaxe/
        background/
    labels/            <- immutable source labels, matched by filename stem

Outputs
-------
dataset/
    images/train/   images/val/   images/test/
    labels/train/   labels/val/   labels/test/
    dataset.yaml
    test_manifest.txt

Split behaviour
---------------
First run  -- splits all data 70/15/15, writes test_manifest.txt.
Later runs -- manifest is respected, new images route to train/val only.

Usage
-----
    python src/shared/prepare_dataset.py
    python src/shared/prepare_dataset.py --target_class hammer
    python src/shared/prepare_dataset.py --val_split 0.15 --test_split 0.15
    python src/shared/prepare_dataset.py --verify_only
    python src/shared/prepare_dataset.py --reset_manifest
"""

from __future__ import annotations

import argparse
import hashlib
import random
import shutil
import sys
from pathlib import Path
from typing import NamedTuple

# Resolve repo root regardless of where the script is called from
ROOT = Path(__file__).parent.parent.parent if Path(__file__).parent.parent.name == "src" else Path(__file__).parent

# ─────────────────────────────────────────────────────────────────────────────
SUPPORTED_IMG   = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}
CLASS_NAMES     = ["hammer", "axe", "wrench", "pickaxe"]
BACKGROUND_DIRS = {"background"}
MANIFEST_FILE   = "test_manifest.txt"

YAML_TEMPLATE = """\
# YOLOv8 dataset configuration — HammerTime
# Auto-generated by prepare_dataset.py
# Mode: {mode}

path: {abs_dataset}
train: images/train
val:   images/val
test:  images/test

nc: {nc}
names: {names}
"""

# ─────────────────────────────────────────────────────────────────────────────

class Sample(NamedTuple):
    image:         Path
    label:         Path   # source label in data/labels/ — never modified
    is_background: bool


def log(msg: str, level: str = "INFO") -> None:
    colours = {"INFO": "\033[94m", "WARN": "\033[93m",
               "ERR":  "\033[91m", "OK":   "\033[92m"}
    c = colours.get(level, "")
    print(f"[{c}{level}\033[0m] {msg}")


# ─────────────────────────────────────────────────────────────────────────────
# Validation
# ─────────────────────────────────────────────────────────────────────────────

def validate_label(path: Path) -> list[str]:
    """Return list of error strings; empty list = valid."""
    errors: list[str] = []
    if not path.exists():
        return ["label file missing"]
    lines = path.read_text().strip().splitlines()
    if not lines:
        return []
    for i, line in enumerate(lines, 1):
        parts = line.strip().split()
        if len(parts) != 5:
            errors.append(f"line {i}: need 5 values, got {len(parts)}")
            continue
        try:
            cls  = int(parts[0])
            vals = [float(v) for v in parts[1:]]
        except ValueError:
            errors.append(f"line {i}: non-numeric values")
            continue
        if cls not in range(len(CLASS_NAMES)):
            errors.append(f"line {i}: unknown class id {cls} (valid: 0-{len(CLASS_NAMES)-1})")
        if not all(0.0 <= v <= 1.0 for v in vals):
            errors.append(f"line {i}: bbox values outside [0,1]")
    return errors


def md5(path: Path, chunk: int = 1 << 20) -> str:
    h = hashlib.md5()
    with open(path, "rb") as f:
        for block in iter(lambda: f.read(chunk), b""):
            h.update(block)
    return h.hexdigest()


# ─────────────────────────────────────────────────────────────────────────────
# Collection
# ─────────────────────────────────────────────────────────────────────────────

def collect(data_dir: Path) -> list[Sample]:
    classes_dir = data_dir / "classes"
    lbl_dir     = data_dir / "labels"

    if not classes_dir.exists():
        log(f"data/classes/ not found at {classes_dir}", "ERR")
        sys.exit(1)

    lbl_dir.mkdir(parents=True, exist_ok=True)
    samples: list[Sample] = []
    invalid = 0

    class_dirs = sorted([d for d in classes_dir.iterdir() if d.is_dir()])
    if not class_dirs:
        log("No subdirectories found in data/classes/", "ERR")
        sys.exit(1)

    for class_dir in class_dirs:
        is_bg_dir = class_dir.name.lower() in BACKGROUND_DIRS
        dir_count = 0

        for img in sorted(class_dir.iterdir()):
            if img.suffix.lower() not in SUPPORTED_IMG:
                continue

            lbl = lbl_dir / (img.stem + ".txt")

            if is_bg_dir:
                if not lbl.exists():
                    lbl.write_text("")
                samples.append(Sample(img, lbl, is_background=True))
                dir_count += 1
                continue

            if not lbl.exists():
                lbl.write_text("")
                log(f"Missing label for {img.name} — created empty (background)", "WARN")

            errs = validate_label(lbl)
            if errs:
                log(f"Bad label {lbl.name}: {'; '.join(errs)}", "WARN")
                invalid += 1
                continue

            is_bg = lbl.read_text().strip() == ""
            samples.append(Sample(img, lbl, is_bg))
            dir_count += 1

        log(f"  {class_dir.name:<12} {dir_count:>4} images")

    n_labeled = sum(not s.is_background for s in samples)
    n_bg      = sum(s.is_background     for s in samples)
    log(f"Collected {len(samples)} samples — {n_labeled} labeled, {n_bg} background")
    if invalid:
        log(f"Skipped {invalid} samples with invalid labels", "WARN")

    return samples


def deduplicate(samples: list[Sample]) -> list[Sample]:
    seen: dict[str, Path] = {}
    out:  list[Sample]    = []
    dupes = 0
    for s in samples:
        h = md5(s.image)
        if h in seen:
            log(f"Duplicate: {s.image.name} == {seen[h].name} — removed", "WARN")
            dupes += 1
        else:
            seen[h] = s.image
            out.append(s)
    if dupes:
        log(f"Removed {dupes} duplicate(s)", "WARN")
    else:
        log("No duplicates found", "OK")
    return out


# ─────────────────────────────────────────────────────────────────────────────
# Test manifest
# ─────────────────────────────────────────────────────────────────────────────

def load_manifest(out_dir: Path) -> set[str] | None:
    p = out_dir / MANIFEST_FILE
    if not p.exists():
        return None
    names = {line.strip() for line in p.read_text().splitlines() if line.strip()}
    log(f"Loaded test manifest — {len(names)} locked test images", "OK")
    return names


def save_manifest(out_dir: Path, test_samples: list[Sample]) -> None:
    p = out_dir / MANIFEST_FILE
    out_dir.mkdir(parents=True, exist_ok=True)
    p.write_text("\n".join(sorted(s.image.name for s in test_samples)) + "\n")
    log(f"Test manifest written → {p}", "OK")


# ─────────────────────────────────────────────────────────────────────────────
# Split
# ─────────────────────────────────────────────────────────────────────────────

def stratified_split(
    samples: list[Sample],
    val_split: float,
    test_split: float,
    seed: int,
    locked_test_names: set[str] | None,
) -> tuple[list[Sample], list[Sample], list[Sample]]:
    rng = random.Random(seed)

    if locked_test_names is not None:
        test     = [s for s in samples if s.image.name in locked_test_names]
        non_test = [s for s in samples if s.image.name not in locked_test_names]

        missing = locked_test_names - {s.image.name for s in test}
        if missing:
            log(f"{len(missing)} manifest image(s) not found in data/classes/", "WARN")
            for name in sorted(missing):
                log(f"  missing: {name}", "WARN")

        new_images = [s for s in non_test if s.image.name not in locked_test_names]
        if new_images:
            log(f"  {len(new_images)} new image(s) — routing to train/val only")

        labeled = [s for s in non_test if not s.is_background]
        bgs     = [s for s in non_test if s.is_background]
        rng.shuffle(labeled); rng.shuffle(bgs)

        effective_val = val_split / (1.0 - test_split)

        def _split(lst):
            n = max(1, round(len(lst) * effective_val)) if lst else 0
            return lst[n:], lst[:n]

        h_tr, h_val = _split(labeled)
        b_tr, b_val = _split(bgs)

    else:
        labeled = [s for s in samples if not s.is_background]
        bgs     = [s for s in samples if s.is_background]
        rng.shuffle(labeled); rng.shuffle(bgs)

        def _carve(lst):
            n_test = max(1, round(len(lst) * test_split)) if lst else 0
            n_val  = max(1, round(len(lst) * val_split))  if lst else 0
            return lst[n_test + n_val:], lst[n_test:n_test + n_val], lst[:n_test]

        h_tr, h_val, h_te = _carve(labeled)
        b_tr, b_val, b_te = _carve(bgs)
        test = h_te + b_te

    train = h_tr + b_tr
    val   = h_val + b_val
    rng.shuffle(train); rng.shuffle(val)

    n_ltr = sum(not s.is_background for s in train)
    n_btr = sum(s.is_background     for s in train)
    n_lv  = sum(not s.is_background for s in val)
    n_bv  = sum(s.is_background     for s in val)
    n_lte = sum(not s.is_background for s in test)
    n_bte = sum(s.is_background     for s in test)

    log(f"Split → train {len(train)} ({n_ltr} labeled+{n_btr}bg) | "
        f"val {len(val)} ({n_lv} labeled+{n_bv}bg) | "
        f"test {len(test)} ({n_lte} labeled+{n_bte}bg)")

    return train, val, test


# ─────────────────────────────────────────────────────────────────────────────
# Label remapping for binary mode
# ─────────────────────────────────────────────────────────────────────────────

def remap_label_binary(source_lbl: Path, target_class_id: int) -> str:
    """
    Read source label, return remapped content for binary mode.
    Lines matching target_class_id are remapped to class 0.
    All other annotation lines are dropped.
    Result may be empty (background) if no target class annotations exist.
    Source file is never modified.
    """
    if not source_lbl.exists() or source_lbl.read_text().strip() == "":
        return ""
    out_lines = []
    for line in source_lbl.read_text().strip().splitlines():
        parts = line.strip().split()
        if len(parts) < 5:
            continue
        if int(parts[0]) == target_class_id:
            out_lines.append("0 " + " ".join(parts[1:]))
    return "\n".join(out_lines)


# ─────────────────────────────────────────────────────────────────────────────
# Write
# ─────────────────────────────────────────────────────────────────────────────

def write_split(
    samples: list[Sample],
    img_dst: Path,
    lbl_dst: Path,
    copy: bool,
    target_class_id: int | None,
) -> None:
    img_dst.mkdir(parents=True, exist_ok=True)
    lbl_dst.mkdir(parents=True, exist_ok=True)

    for s in samples:
        # Always copy image
        dest_img = img_dst / s.image.name
        if copy:
            shutil.copy2(s.image, dest_img)
        else:
            if not dest_img.exists():
                dest_img.symlink_to(s.image.resolve())

        # Write label
        dest_lbl = lbl_dst / (s.image.stem + ".txt")
        if target_class_id is not None:
            # Binary mode: remap in memory, write derived copy
            content = remap_label_binary(s.label, target_class_id)
            dest_lbl.write_text(content)
        else:
            # Multi-class mode: symlink or copy source label
            if copy:
                shutil.copy2(s.label, dest_lbl)
            else:
                if not dest_lbl.exists():
                    dest_lbl.symlink_to(s.label.resolve())


def write_yaml(dataset_dir: Path, mode: str, class_names: list[str]) -> Path:
    p = dataset_dir / "dataset.yaml"
    p.write_text(YAML_TEMPLATE.format(
        mode        = mode,
        abs_dataset = dataset_dir.resolve(),
        nc          = len(class_names),
        names       = class_names,
    ))
    return p


def print_summary(dataset_dir: Path, mode: str, target_class: str | None) -> None:
    print("\n" + "─" * 56)
    print(f"  DATASET READY  [{mode.upper()}]")
    if target_class:
        print(f"  Target class : {target_class} (all others treated as background)")
    print("─" * 56)
    for split in ("train", "val", "test"):
        img_dir = dataset_dir / "images" / split
        lbl_dir = dataset_dir / "labels" / split
        if not img_dir.exists():
            continue
        imgs = list(img_dir.glob("*"))
        lbls = list(lbl_dir.glob("*.txt"))
        bgs  = sum(1 for l in lbls if l.read_text().strip() == "")
        marker = " <- LOCKED" if split == "test" else ""
        print(f"  {split:5s}  {len(imgs):4d} images  "
              f"{len(lbls)-bgs:4d} labeled  {bgs:4d} background{marker}")
    print(f"\n  YAML     -> {dataset_dir / 'dataset.yaml'}")
    print(f"  Manifest -> {dataset_dir / MANIFEST_FILE}")
    print("─" * 56 + "\n")


# ─────────────────────────────────────────────────────────────────────────────
# CLI
# ─────────────────────────────────────────────────────────────────────────────

def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Prepare HammerTime dataset for binary or multi-class training",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument("--data_dir",       default=str(ROOT / "data"),    help="Folder with classes/ and labels/")
    p.add_argument("--out_dir",        default=str(ROOT / "dataset"), help="Output split folder")
    p.add_argument("--target_class",   default=None,
                   choices=CLASS_NAMES,
                   help="Binary mode: class to treat as positive. All others become background.")
    p.add_argument("--val_split",      type=float, default=0.15)
    p.add_argument("--test_split",     type=float, default=0.15)
    p.add_argument("--seed",           type=int,   default=42)
    p.add_argument("--copy",           action="store_false", dest="symlink", default=True,
                                       help="Copy files instead of symlinking")
    p.add_argument("--no_dedup",       action="store_true",  help="Skip duplicate removal")
    p.add_argument("--verify_only",    action="store_true",  help="Validate only, do not split")
    p.add_argument("--reset_manifest", action="store_true",
                                       help="Delete manifest and re-randomise test set")
    return p.parse_args()


def main() -> None:
    print("\n╔══════════════════════════════════════════════╗")
    print("║  HammerTime — Dataset Preparation          ║")
    print("╚══════════════════════════════════════════════╝\n")

    args     = parse_args()
    data_dir = Path(args.data_dir)
    out_dir  = Path(args.out_dir)

    if args.val_split + args.test_split >= 1.0:
        log("val_split + test_split must be < 1.0", "ERR")
        sys.exit(1)

    # Determine mode
    target_class    = args.target_class
    target_class_id = CLASS_NAMES.index(target_class) if target_class else None
    mode            = f"binary:{target_class}" if target_class else "multi-class"
    log(f"Mode: {mode}")

    samples = collect(data_dir)

    if not args.no_dedup:
        samples = deduplicate(samples)

    if len(samples) < 10:
        log("Fewer than 10 valid samples — aborting.", "ERR")
        sys.exit(1)

    if args.verify_only:
        log("--verify_only set. Skipping split.", "OK")
        return

    manifest_path = out_dir / MANIFEST_FILE

    if args.reset_manifest and manifest_path.exists():
        manifest_path.unlink()
        log("Manifest deleted — test set will be re-randomised.", "WARN")

    locked_test_names = load_manifest(out_dir)
    first_run = locked_test_names is None

    if first_run:
        log("No manifest found — performing initial 70/15/15 split.")
    else:
        log("Manifest found — test set is locked. New images -> train/val only.")

    train, val, test = stratified_split(
        samples, args.val_split, args.test_split, args.seed, locked_test_names
    )

    for split in ("train", "val", "test"):
        for sub in ("images", "labels"):
            d = out_dir / sub / split
            if d.exists():
                shutil.rmtree(d)

    use_copy = not args.symlink
    write_split(train, out_dir/"images"/"train", out_dir/"labels"/"train", use_copy, target_class_id)
    write_split(val,   out_dir/"images"/"val",   out_dir/"labels"/"val",   use_copy, target_class_id)
    write_split(test,  out_dir/"images"/"test",  out_dir/"labels"/"test",  use_copy, target_class_id)

    # YAML uses single class for binary, full list for multi
    yaml_classes = [target_class] if target_class else CLASS_NAMES
    write_yaml(out_dir, mode, yaml_classes)

    if first_run:
        save_manifest(out_dir, test)

    print_summary(out_dir, mode, target_class)
    log(f"Done. Next: python src/binary/train_b.py  or  python src/multi/train_m.py", "OK")


if __name__ == "__main__":
    main()
